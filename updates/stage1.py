# Константа
FIXED_RENT = 30000

print("=== Учет расходов за квартиру ===")

# 1. Ввод данных

# Добавим месяц и проверим, чтобы использовались валидные занчения

# Словарь с названиями месяцев
month_names = {
    1: "Январь", 2: "Февраль", 3: "Март", 4: "Апрель",
    5: "Май", 6: "Июнь", 7: "Июль", 8: "Август",
    9: "Сентябрь", 10: "Октябрь", 11: "Ноябрь", 12: "Декабрь"
}

# Основной цикл ввода
while True:
    # Запрос ввода
    month_input = input("Введите номер месяца (от 01 до 12): ")

    # 1. Проверка: не пустая строка
    month_clean = month_input.strip()  # Убираем пробелы
    if not month_clean:  # Если строка пустая
        print("Ошибка: Вы не ввели месяц. Попробуйте еще раз.\n")
        continue

        # 2. Проверка: только цифры
    if not month_clean.isdigit():
        print(f"Ошибка: '{month_input}' содержит не только цифры. Попробуйте еще раз.\n")
        continue

        # 3. Преобразование в число
    month_num = int(month_clean)
    
    # 4. Проверка диапазона
    if month_num < 1:
        print(f"Ошибка: {month_num} меньше 1. Попробуйте еще раз.\n")
        continue
    
    if month_num > 12:
        print(f"Ошибка: {month_num} больше 12. Попробуйте еще раз.\n")
        continue
    
    # Все проверки пройдены!

        # Форматируем вывод: делаем двузначный формат
    month_display = str(month_num).zfill(2)  # "1" -> "01"

        # Получаем название месяца из словаря
    month_name = month_names[month_num]

        # Выводим результат
    print(f"\n✓ Успешно! Вы выбрали:")
    print(f"  Число: {month_display}")
    print(f"  Название: {month_name}")

        # Сохраняем в переменную для дальнейшего использования
    final_month = month_display
    final_month_name = month_name
    
    break  # Выходим из цикла

# Выводим финальный результат
print(f"\n=== РЕЗУЛЬТАТ ===")
print(f"Месяц для учета расходов: {final_month} ({final_month_name})")

# Ожидаем нажатия Enter перед завершением
input("\nНажмите Enter для выхода...")


